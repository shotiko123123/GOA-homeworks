# რიცხვების ჯამი(შეინახეთ ცალკე ცვლადებში), გაამრავლეთ ორივე ერთმანეთზე და დააბრუნეთ
def lis(list1, list2):
    sum1 = 0
    for num in list1:
        sum1 += num

    sum2 = 0
    for num in list2:
        sum2 += num

    result = sum1 * sum2
    return result