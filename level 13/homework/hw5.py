age = int(input('Enter Your age: '))

if 0 <= age <= 12:
    print("რეკომენდაცია: ბევრი ვიტამინიანი საკვები")
elif 13 <= age <= 25:
    print("რეკომენდაცია: ენერგიის მომცემი საკვები")
elif 26 <= age <= 59:
    print("რეკომენდაცია: ბალანსირებული რაციონი")
elif age >= 60:
    print("რეკომენდაცია: დაბალკალორიული და მსუბუქი საკვები")
